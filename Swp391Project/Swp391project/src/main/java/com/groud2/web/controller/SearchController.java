    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        String category = request.getParameter("categoryname");
        System.out.println(category);
        newsDAO nes = new newsDAO();
        ArrayList<news> listcategory;
        try {
            listcategory = nes.getCategory(category);
            request.setAttribute("listcategory", listcategory);
            request.setAttribute("category", category);
            request.getRequestDispatcher("ListNews.jsp").forward(request, response);
        } catch (SQLException ex) {
            Logger.getLogger(SearchController.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String search = request.getParameter("search_value");
        newsDAO n = new newsDAO();
        ArrayList<news> listSearchValue;
        System.out.println(search);
        try {
            listSearchValue = n.getchar(search);
            request.setAttribute("search", search);
            request.setAttribute("listSearchValue", listSearchValue);
            request.getRequestDispatcher("ListNews.jsp").forward(request, response);
        } catch (SQLException ex) {
            Logger.getLogger(SearchController.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

